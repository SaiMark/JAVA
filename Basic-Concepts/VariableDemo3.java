public class VariableDemo3 {
	public static void main (String[] args) {
		//byte
		byte b = 10;
		System.out.println(b);//10
		//short
		short s = 20;
		System.out.println(s);//20
		//int
		int i = 30;
		System.out.println(i);//30
		//long
		//if define long as a variable
		//data need to putdown L at the end of the data
		//L can be capital letter, can be smaller letter
		//Suggestion: Use capital L
		long n = 9999999999L;
		System.out.println(n);


		//float
		//When define float 
		//Data need F in the end of data
		float f = 10.1F;
		System.out.println(f);

		//double
		double d = 20.2;
		System.out.println(d);

		//char
		char c ='A';
		System.out.println(c);

		//Boolean
		boolean o = true;
		System.out.println(o);
	}
}